<template>
  <div>
    <v-container fluid class="wrapper">
      <v-row>
        <v-col cols="6">
          <v-autocomplete
            ref="input"
            v-model="lookup.table"
            outlined
            class="caption"
            hide-details="auto"
            :label="$t('labels.childTable')"
            :full-width="false"
            :items="refTables"
            item-text="title"
            :item-value="v => v"
            :rules="[v => !!v || 'Required']"
            dense
          >
            <template #item="{item}">
              <span class="caption"><span class="font-weight-bold"> {{
                item.title || item.table_name
              }}</span> <small>({{ relationNames[item.col.type] }})
              </small></span>
            </template>
          </v-autocomplete>
        </v-col>
        <v-col cols="6">
          <v-autocomplete
            ref="input"
            v-model="lookup.column"
            outlined
            class="caption"
            hide-details="auto"
            :label="$t('labels.childColumn')"
            :full-width="false"
            :items="columnList"
            item-text="title"
            dense
            :loading="loadingColumns"
            :item-value="v => v"
            :rules="[v => !!v || 'Required',checkLookupExist]"
          />
        </v-col>
      </v-row>
    </v-container>
  </div>
</template>

<script>

import { isSystemColumn, UITypes } from 'nocodb-sdk'

export default {
  name: 'LookupOptions',
  props: ['nodes', 'column', 'meta', 'isSQLite', 'alias'],
  data: () => ({
    lookup: {},
    loadingColumns: false,
    relationNames: {
      mm: 'Many To Many',
      hm: 'Has Many',
      bt: 'Belongs To'
    },
    tables: []
  }),
  computed: {
    refTables() {
      if (!this.tables || !this.tables.length) { return [] }

      const refTables = this.meta.columns.filter(c =>
        c.uidt === UITypes.LinkToAnotherRecord && !c.system
      ).map(c => ({
        col: c.colOptions,
        ...this.tables.find(t => t.id === c.colOptions.fk_related_model_id)
      }))

      return refTables

      // return (this.meta
      //   ? [
      //       ...(this.meta.belongsTo || []).map(({ rtn, _rtn, rcn, table_name, cn }) => ({
      //         type: 'bt',
      //         rtn,
      //         _rtn,
      //         rcn,
      //         table_name,
      //         cn,
      //         ltn: rtn,
      //         _ltn: _rtn
      //       })),
      //       ...(this.meta.hasMany || []).map(({
      //         table_name,
      //         title,
      //         cn,
      //         rcn,
      //         rtn
      //       }) => ({
      //         type: 'hm',
      //         table_name,
      //         title,
      //         cn,
      //         rcn,
      //         rtn,
      //         ltn: table_name,
      //         _ltn: title
      //       })),
      //       ...(this.meta.manyToMany || []).map(({ vtn, _vtn, vrcn, vcn, rtn, _rtn, rcn, table_name, cn }) => ({
      //         type: 'mm',
      //         table_name,
      //         cn,
      //         vtn,
      //         _vtn,
      //         vrcn,
      //         rcn,
      //         rtn,
      //         vcn,
      //         _rtn,
      //         ltn: rtn,
      //         _ltn: _rtn
      //       }))
      //     ]
      //   : []).filter(t => this.tables.includes(t.ltn))
    },
    columnList() {
      return ((
        this.lookup &&
        this.lookup.table &&
        this.$store.state.meta.metas &&
        this.$store.state.meta.metas[this.lookup.table.id] &&
        this.$store.state.meta.metas[this.lookup.table.id].columns
      ) || []).filter(c => !isSystemColumn(c))
    }
  },
  async mounted() {
    await this.loadTablesList()
  },
  methods: {
    async loadTablesList() {
      const result = (await this.$api.dbTable.list({
        projectId: this.$store.state.project.projectId,
        baseId: this.$store.state.project.project.bases[0].id
      }))

      //   await this.$store.dispatch('sqlMgr/ActSqlOp', [{
      //   env: this.nodes.env,
      //   dbAlias: this.nodes.dbAlias
      // }, 'tableList'])

      this.tables = result.list
    },
    checkLookupExist(v) {
      return (this.lookup.table && (this.meta.v || []).every(c => !(
        c.lk &&
        c.lk.type === this.lookup.table.type &&
        c.lk.ltn === this.lookup.table.ltn &&
        c.lk.lcn === v.lcn
      ))) || 'Lookup already exist'
    },
    async onTableChange() {
      this.loadingColumns = true
      if (this.lookup.table) {
        try {
          await this.$store.dispatch('meta/ActLoadMeta', {
            dbAlias: this.nodes.dbAlias,
            env: this.nodes.env,
            id: this.lookup.table.id
          })
        } catch (e) {
          // ignore
        }
      }

      this.loadingColumns = false
    },
    async save() {
      try {
        // await this.$store.dispatch('meta/ActLoadMeta', {
        //   dbAlias: this.nodes.dbAlias,
        //   env: this.nodes.env,
        //   table_name: this.meta.table_name,
        //   force: true
        // })
        // const meta = JSON.parse(JSON.stringify(this.$store.state.meta.metas[this.meta.table_name]))

        // meta.v.push({
        //   _cn: this.alias,
        //   lk: {
        //     ...this.lookup.table,
        //     ...this.lookup.column
        //   }
        // })

        console.log(this.lookup)

        const lookupCol = {
          title: this.alias,
          fk_relation_column_id: this.lookup.table.col.fk_column_id,
          fk_lookup_column_id: this.lookup.column.id,
          uidt: UITypes.Lookup
        }

        await this.$api.dbTable.create(this.meta.id, lookupCol)

        // await this.$store.dispatch('sqlMgr/ActSqlOp', [{
        //   env: this.nodes.env,
        //   dbAlias: this.nodes.dbAlias
        // }, 'xcModelSet', {
        //   table_name: this.nodes.table_name,
        //   meta
        // }])

        return this.$emit('saved', this.alias)
      } catch (e) {
        console.log(e)
        this.$toast.error(e.message).goAway(3000)
      }
    }
  }
}
</script>

